/**
 * Hook or helper to safely fetch dynamic translation text for a given key and active language.
 */
export function getTranslation(key: string, lang: string): string {
  if (!lang || lang === "en") {
    return TRANSLATE_DICTIONARY.en[key] || key;
  }
  const dict = TRANSLATE_DICTIONARY[lang];
  if (dict && dict[key]) {
    return dict[key];
  }
  // Fallback to English, then to key itself
  return TRANSLATE_DICTIONARY.en[key] || key;
}

/**
 * Format Indian Rupees with comma separation (Lakh / Crore format) or standard currency.
 */
export function formatCurrency(amount: number, lang: string): string {
  // If India or regional language, use Indian currency format
  const isIndianLocale = ["te", "hi", "ur", "ta", "ml", "kn"].includes(lang);
  if (isIndianLocale) {
    return new Intl.NumberFormat("en-IN", {
      style: "currency",
      currency: "INR",
      maximumFractionDigits: 0
    }).format(amount);
  }
  return new Intl.NumberFormat("en-US", {
    style: "currency",
    currency: "USD",
    maximumFractionDigits: 0
  }).format(amount);
}
